package PageFactoryPom.Utilities;


public class ItemDetails {
    public String itemName;
    public String price;
    public String quant;
    public String size;
    public String col;
    public String num;
    public String numTwo;

    public ItemDetails(String item, String price, String quant, String size, String col, String num, String numTwo) {
        this.itemName = item;
        this.price = price;
        this.quant = quant;
        this.size = size;
        this.col = col;
        this.num = num;
        this.numTwo = numTwo;
    }
}
